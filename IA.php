<script>
// Gestion des sections "À explorer"
document.querySelectorAll('.ai-exploration-btn').forEach(btn => {
    btn.addEventListener('click', function() {
        const content = this.nextElementSibling;
        content.style.display = content.style.display === 'none' ? 'block' : 'none';
        this.classList.toggle('active');
    });
});

// Gestion du feedback
document.querySelector('.feedback-toggle').addEventListener('click', function() {
    document.querySelector('.feedback-form').classList.toggle('active');
});

// Initialisation
document.addEventListener('DOMContentLoaded', function() {
    // Masquer tous les contenus d'exploration au chargement
    document.querySelectorAll('.ai-exploration-content').forEach(content => {
        content.style.display = 'none';
    });
    
    // Masquer le formulaire de feedback au chargement
    document.querySelector('.feedback-form').style.maxHeight = '0';
});
</script>